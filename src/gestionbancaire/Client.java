    private void tableClient()
    {
        int c;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost:3306/gestionbancaire?zeroDateTimeBehavior=CONVERT_TO_NULL","root","");
            ajoutClient=con1.prepareStatement("SELECT * FROM Client");
            ResultSet rs=ajoutClient.executeQuery();
            ResultSetMetaData Res=rs.getMetaData();
            c=Res.getColumnCount();
            DefaultTableModel Df=(DefaultTableModel)TableClient.getModel();
            Df.setRowCount(0);
            while(rs.next())
            {
                Vector v2=new Vector();
                for(int a=1; a<=c; a++)
                {
                    v2.add(rs.getString("NumCompte"));
                    v2.add(rs.getString("Nom"));
                    v2.add(rs.getString("Adresse"));
                    v2.add(rs.getInt("Solde"));
                }
                Df.addRow(v2);
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Client.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    
    }
