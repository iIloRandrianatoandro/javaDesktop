             while (rs.next()) {
                    ancienSoldeInt=rs.getInt("Solde");
            }
         
           
            int nouveauSoldeInt=ancienSoldeInt-Integer.parseInt(montant);
            nouveauSolde = con1.prepareStatement("UPDATE Client SET Solde='"+nouveauSoldeInt+"' WHERE NumCompte='"+numcompte+"'");
